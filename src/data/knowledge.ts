export function getCategoryById(id: string): Category | undefined {
  return categories.find((c) => c.id === id)
}

export function getItemById(categoryId: string, itemId: string): KnowledgeItem | undefined {
  const category = getCategoryById(categoryId)
  return category?.items.find((i) => i.id === itemId)
}

export function searchKnowledge(query: string): { category: Category; item: KnowledgeItem }[] {
  const q = query.toLowerCase().trim()
  if (!q) return []
  const results: { category: Category; item: KnowledgeItem }[] = []
  for (const category of categories) {
    for (const item of category.items) {
      const match =
        item.title.toLowerCase().includes(q) ||
        item.summary.toLowerCase().includes(q) ||
        item.tags.some((t) => t.toLowerCase().includes(q)) ||
        item.content.some((c) => c.toLowerCase().includes(q)) ||
        (item.cases || []).some((c) => c.toLowerCase().includes(q)) ||
        (item.pmApplication || []).some((c) => c.toLowerCase().includes(q))
      if (match) results.push({ category, item })
    }
  }
  return results
}
